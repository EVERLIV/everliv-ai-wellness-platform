// Функция для получения биомаркера по ID
export const getBiomarkerById = (id: string): BiomarkerKnowledge | undefined => {
  return BIOMARKER_KNOWLEDGE.find(biomarker => biomarker.id === id);
};

// Функция для получения биомаркеров по категории
export const getBiomarkersByCategory = (category: string): BiomarkerKnowledge[] => {
  return BIOMARKER_KNOWLEDGE.filter(biomarker => biomarker.category === category);
};

// Функция для поиска биомаркеров
export const searchBiomarkers = (query: string): BiomarkerKnowledge[] => {
  const searchTerm = query.toLowerCase();
  return BIOMARKER_KNOWLEDGE.filter(biomarker => 
    biomarker.name.toLowerCase().includes(searchTerm) ||
    biomarker.alternativeNames?.some(name => name.toLowerCase().includes(searchTerm)) ||
    biomarker.tags.some(tag => tag.toLowerCase().includes(searchTerm)) ||
    biomarker.description.toLowerCase().includes(searchTerm)
  );
};